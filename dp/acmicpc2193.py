N = int(input())
if N == 1:
    print(1)
    exit()

d = [0]*(N+1)
d[1] = (0, 1)
'''
d[1] = 1 ==> 0: 0, 1: 1
d[2] = 10 ==> 0: 1, 1: 0
d[3] = 10 0, 10 1 ==> 0: 1, 1: 1
d[4] = 101 0, 100 0, 100 1 ==> 0: 2, 1: 1
d[5] = 1010 0, 1010 1, 1000 0, 1000 1, 1001 0 ==> 0: 3, 1: 2
d[6] = 10100 0, 10100 1, 10101 0, 10000 0, 10000 1, 10001 0, 10010 1, 10010 0 ==> 0: 5, 1: 3
'''
for i in range(2, N+1):
    zero_prev, one_prev = d[i-1][0], d[i-1][1]
    d[i] = (zero_prev+one_prev, zero_prev)

print(sum(d[N]))